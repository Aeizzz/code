/*

牌型种数

小明被劫持到X赌城，被迫与其他3人玩牌。
一副扑克牌（去掉大小王牌，共52张），均匀发给4个人，每个人13张。
这时，小明脑子里突然冒出一个问题：
如果不考虑花色，只考虑点数，也不考虑自己得到的牌的先后顺序，自己手里能拿到的初始牌型组合一共有多少种呢？

请填写该整数，不要填写任何多余的内容或说明文字。

*/




#include<cstdio>
#include<algorithm>
using namespace std;
//num是牌的张数
//cur参数是牌的种类
int num=0;
int sum=0;
void dfs(int cur){
    if(num>13) return ;
    if(cur==13){
        if(num==13){
            sum++;

        }
        return ;
    }
    for(int i=0;i<5;i++){
        num+=i;
        dfs(cur+1);
        num-=i;
    }

}





int main(){
    dfs(0);
    printf("%d\n",sum);

    return 0;
}
